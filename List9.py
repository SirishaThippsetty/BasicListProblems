'''
Python provides us with various ways of reversing a list. 
We will go through some of the many techniques on how a list 
in Python can be reversed.

Example: 

Input: list = [4, 5, 6, 7, 8, 9]
Output : [9, 8, 7, 6, 5, 4] 

'''

input_list = [4, 5, 6, 7, 8, 9]
print("Reverse list:",input_list[::-1])